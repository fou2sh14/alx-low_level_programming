#include "3-calc.h"

/**
 * op_add - sum function
 *
 * @a: first num
 *
 * @b: second num
 *
 * Return: sum of them
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract fun
 *
 * @a: first num
 *
 * @b: second num
 *
 * Return: substraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply fun
 *
 * @a: first num
 *
 * @b: second num
 *
 * Return: multiply
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 *
 * @a: first
 *
 * @b: second
 *
 * Return: division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus
 *
 * @a: first
 *
 * @b: second
 *
 * Return: modulus
 */

int op_mod(int a, int b)
{
	return (a % b);
}
